async function buy(amount){
  amount = ethers.utils.parseEther(amount);
  await contract.buy({ value: amount });
}

async function sell(amount){
  await contract.sell(amount);
}

async function possiblePrice(){
  document.getElementById("possiblePriceBox").value =  ethers.utils.formatEther((await contract.possiblePrice()).toString());
}

async function balance(){
  document.getElementById("OlxAmount").value =  (await contract.balanceOf(await signer.getAddress())).toString();
}

